    {
        $bookers = DB::table('books')->where('people_id',$id)->get();
        foreach($bookers as $booker){
            \App\Models\Book::find($booker->id)->delete();
        }

        \App\Models\People::find($id)->delete();

        return redirect('/emp'); 
